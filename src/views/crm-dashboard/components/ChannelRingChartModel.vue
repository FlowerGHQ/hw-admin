<template>
    <div class="list-container">
        <!-- 标题 -->
        <div class="title">
            <div>{{ title }}</div>
            <div class="detail-title" @click="goToDetail('detail')">
                详情
            </div>
        </div>
        <!-- echarts -->
        <div class="table-container">
            <template v-if="isEmpty">
                <div id="ChannelRingChartId" class="chart" ref='ChannelRingChartId'></div>
                <div class="legend-container">
                    <div class="legend-wrap" v-for="item in legendList">
                        <div class="legend-block">
                            <div class="legend-circle" :style="{ backgroundColor: item.color }"></div>
                            <div class="legend-key">{{ item?.item }}</div>
                        </div>
                        <div class="legend-value">{{ item?.percent }}</div>
                    </div>
                </div>
            </template>
            <template v-else>
                <div class="empty-wrap">
                    <img src="../../../assets/images/dashboard/emptyData.png" alt="">
                    <div class="empty-desc">
                        暂无数据
                    </div>
                </div>
            </template>
        </div>
    </div>
</template>

<script>
import { Chart } from '@antv/g2'
import Core from "../../../core";
import data from '../../../core/data';

export default {
    name: 'Cards',
    components: {
    },
    props: {
        searchForm: {
            type: Object,
            default: () => { }
        },
        title: {
            type: String,
            default: '来源'
        }
    },
    data() {
        return {
            currentTab: '',
            myChart: null,
            boStatisticsChart: {},
            groupStatusTableData: [],
            legendList: [],
            title: '来源',
            SOURCE_TYPE_MAP: Core.Const.VOTE.SOURCE_TYPE_MAP,
            isEmpty: false,
        };
    },
    watch: {
        searchForm: {
            deep: true,
            immediate: true,
            handler(n) {
                this.getChannelChartData()
            }
        },

    },
    computed: {
        lang() {
            return this.$store.state.lang
        },
    },
    created() {

    },
    mounted() {
        this.getChannelChartData();
    },
    beforeUnmount() {
        this.$refs.ChannelRingChartId.innerHTML = ''
    },
    methods: {
        drawBoStatisticsChart(data) {
            if (this.boStatisticsChart.destroy) {
                console.log('drawPurchaseChart destroy:')
                this.boStatisticsChart.destroy()
            }
            const chart = new Chart({
                container: 'ChannelRingChartId',
                autoFit: true,
                height: 274,
            });
            // 新建一个 view 用来单独渲染Annotation
            const innerView = chart.createView();
            chart.data(data);
            chart.scale('percent', {
                formatter: val => {
                    val = val + '%';
                    return val;
                },
            });
            chart.tooltip(false);
            chart.coordinate('theta', {
                radius: 0.7,
                innerRadius: 0.8,
            });
            // 声明需要进行自定义图例字段： 'item'
            chart.legend(false);
            // 监听 element 上状态的变化来动态更新 Annotation 信息
            chart.on('element:statechange', (ev) => {
                const { state, stateStatus, element } = ev.gEvent.originalEvent;

                // 本示例只需要监听 active 的状态变化
                if (state === 'active') {
                    const data = element.getData();
                    if (stateStatus) {
                        // 更新 Annotation
                        updateAnnotation(data);
                    } else {
                        // 隐藏 Annotation
                        clearAnnotation();
                    }
                }
            });

            // 绘制 annotation
            let lastItem;
            function updateAnnotation(data) {
                if (data.item !== lastItem) {
                    innerView.annotation().clear(true);
                    innerView
                        .annotation()
                        .text({
                            position: ['50%', '46%'],
                            content: data.count + '人',
                            style: {
                                fontSize: 14,
                                fontWeight: 600,
                                fill: '#1D2129',
                                textAlign: 'center',
                            },
                        })
                        .text({
                            position: ['50%', '56%'],
                            content: data.item,
                            style: {
                                fontSize: 14,
                                fill: '#333333',
                                textAlign: 'center',
                            },
                        })
                    innerView.render(true);
                    lastItem = data.item;
                }
            }

            // 清空 annotation
            function clearAnnotation() {
                innerView.annotation().clear(true);
                innerView.render(true);
                lastItem = null;
            }

            chart
                .interval()
                .adjust('stack')
                .position('percent')
                .color('item', ['#056DFF', '#FFBC48', '#FB6381', '#15BFEF', '#26D0A1', '#A880FF', '#FF9834', '#5282FF'])
                .style({
                    fillOpacity: 1,
                })
                .state({
                    active: {
                        style: element => {
                            const shape = element.shape;
                            return {
                                lineWidth: 10,
                                stroke: shape.attr('fill'),
                                strokeOpacity: shape.attr('fillOpacity'),
                            };
                        },
                    },
                });
            // 移除图例点击过滤交互
            chart.removeInteraction('legend-filter');
            chart.interaction('element-active');
            chart.render();
            this.boStatisticsChart = chart
        },
        async getChannelChartData() {
            try {
                const res = await Core.Api.VoteData.cityStatistics({ ...this.searchForm });
                console.log('getChannelChartData res', res);
                const formattedData = [];
                const typeCounts = {};
                /* mock */
                // const data = [
                //     {
                //         date: 1692242388,
                //         source_list: [
                //             {
                //                 type: 1,
                //                 vote_count: 500,
                //             },
                //             {
                //                 type: 2,
                //                 vote_count: 400,
                //             },
                //             {
                //                 type: 3,
                //                 vote_count: 300,
                //             },
                //         ]
                //     },
                //     {
                //         date: 1692242388,
                //         source_list: [
                //             {
                //                 type: 1,
                //                 vote_count: 200,
                //             },
                //             {
                //                 type: 2,
                //                 vote_count: 300,
                //             },
                //             {
                //                 type: 3,
                //                 vote_count: 400,
                //             },

                //         ]
                //     },
                //     {
                //         date: 1692242388,
                //         source_list: [
                //             {
                //                 type: 1,
                //                 vote_count: 200,
                //             },
                //             {
                //                 type: 2,
                //                 vote_count: 400,
                //             },
                //             {
                //                 type: 3,
                //                 vote_count: 300,
                //             },
                //         ]
                //     },
                // ]
                res.forEach((item) => {
                    item.source_list.forEach((source) => {
                        const type = source.type || '其他';
                        const voteCount = source.vote_count || 0;
                        if (typeCounts[type]) {
                            typeCounts[type] += voteCount;
                        } else {
                            typeCounts[type] = voteCount;
                        }
                    });
                });
                const totalVotes = Object.values(typeCounts).reduce((sum, count) => sum + count, 0);
                Object.entries(typeCounts).forEach(([type, count]) => {
                    const text = this.SOURCE_TYPE_MAP[type]?.text;
                    const percent = ((count / (totalVotes + 1)) * 100).toFixed(2);
                    formattedData.push({ item: text, count, percent: parseFloat(percent) });
                });
                const color = ['#056DFF', '#FFBC48', '#FB6381', '#15BFEF', '#26D0A1', '#A880FF', '#FF9834', '#5282FF'] // 配置项的颜色
                this.legendList = formattedData
                this.legendList.forEach((item, index) => {
                    item.color = color[index + 1]
                    item.percent = item.percent + '%'
                })
                if (formattedData.length) {
                    this.isEmpty = true
                }
                this.drawBoStatisticsChart(formattedData)
            } catch (error) {
                console.log('Error in getChannelChartData err', error);
            }
        },
        goToDetail(type) {
            let routeUrl = ''
            switch (type) {
                case 'detail':    // 编辑
                    routeUrl = this.$router.resolve({
                        path: "/crm-dashboard/vote-detail",
                        query: {
                            title: this.title
                        }
                    })
                    window.open(routeUrl.href, '_blank')
                    break;
            }
        }
    }
};
</script>

