<template>
<div class="ProductionItem gray-panel no-margin">
    <div class="panel-title">
        <div class="title">已生产产品列表</div>
        <div class="btn-area">
            <!-- <a-button type="primary" @click="handleEntityPutIn" class="panel-btn" v-if="remain_count > 0"><i class="icon i_add"/>产品入库</a-button> -->
            <a-button type="primary" @click="routerChange('put_in')" class="panel-btn" v-if="remain_count > 0"><i class="icon i_add"/>产品入库</a-button>
        </div>
    </div>
    <div class="panel-content">
        <div class="table-container">
            <a-table :columns="tableColumns" :data-source="tableData" :scroll="{ x: true }"
                :row-key="record => record.id" :pagination='false'>
                <template #bodyCell="{ column, text, record }">
                    <template v-if="column.key === 'detail'">
                        <div class="table-img">
                            <a-image :width="24" :height="24" :src="$Util.imageFilter(record.item.logo)" fallback='无'/>
                            <a-tooltip placement="top" :title='text'>
                                <p class="ell" style="max-width:120px;margin-left:12px;">{{text || '-'}}</p>
                            </a-tooltip>
                        </div>
                    </template>
                    <template v-if="column.key === 'item'">
                        {{ text || '-' }}
                    </template>
                    <template v-if="column.key === 'time'">
                        {{ $Util.timeFilter(text) }}
                    </template>
                    <template v-if="column.key === 'operation'">
                        <a-button type="link" @click="handleDelete(record.id)" class="danger"><i class="icon i_delete"/>删除</a-button>
                    </template>
                </template>
            </a-table>
        </div>
    </div>
</div>
</template>

<script>
import Core from '../../../core';
const STOCK_RECORD = Core.Const.STOCK_RECORD
export default {
    name: 'ProductionItem',
    components: {},
    props: {
        id: {
            type: Number,
        },
        uid: {
            type: String,
        },
        detail: {
            type: Object,
        },
    },
    data() {
        return {
            // 加载
            loading: false,
            // 分页
            currPage: 1,
            pageSize: 20,
            total: 0,
            tableData: []
        };
    },
    watch: {},
    computed: {
        tableColumns() {
            let tableColumns = [
                { title: '产品名称', dataIndex: ['item','name'], key: 'detail'},
                { title: '产品编号', dataIndex: 'item_code', key: 'item' },
                { title: '产品实例号', dataIndex: 'uid', key: 'item'},
                { title: '创建时间', dataIndex: 'create_time', key: 'time' },
                { title: '操作', key: 'operation', fixed: 'right'},
            ]
            return tableColumns
        },
        remain_count() {
            return this.detail.amount - this.tableData.length
        }
    },
    mounted() {
        this.getTableData();
    },
    methods: {
        routerChange(type, item = {}) {
            let routeUrl = ''
            switch (type) {
                case 'put_in':    // 实例入库
                    let source = {
                        sourceUid: this.uid,
                        form: {
                            type: STOCK_RECORD.TYPE.IN,
                            target_type: STOCK_RECORD.COMMODITY_TYPE.ENTITY,
                            source_id: this.id,
                            source_type: STOCK_RECORD.SOURCE_TYPE.PRODUCTION,
                            warehouse_id: this.detail.warehouse_id,
                        },
                    }
                    console.log('source:', source)
                    routeUrl = this.$router.resolve({
                        path: "/warehouse/invoice-edit",
                        query: {
                            source: JSON.stringify(source)
                        }
                    })
                    break;
                case 'invoice':
                    routeUrl = this.$router.resolve({
                        path: "/warehouse/invoice-detail",
                        query: { id: item.id }
                    })
                    break;
            }
            window.open(routeUrl.href, '_blank')
        },
        pageChange(curr) {  // 页码改变
            this.currPage = curr
            this.getTableData()
        },
        pageSizeChange(current, size) {  // 页码尺寸改变
            console.log('pageSizeChange size:', size)
            this.pageSize = size
            this.getTableData()
        },
        getTableData() {  // 获取 表格 数据
            this.loading = true;
            Core.Api.Entity.list({
                production_order_id: this.id,
                page: this.currPage,
                page_size: this.pageSize
            }).then(res => {
                console.log("getTableData res", res)
                this.total = res.count;
                this.tableData = res.list;
            }).catch(err => {
                console.log('getTableData err', err)
            }).finally(() => {
                this.loading = false;
            });
        },
        // 创建入库单
        handleEntityPutIn() {
            let form = {
                type: STOCK_RECORD.TYPE.IN,
                target_type: STOCK_RECORD.COMMODITY_TYPE.ENTITY,
                warehouse_id: this.detail.warehouse_id,
                source_type: STOCK_RECORD.SOURCE_TYPE.PRODUCTION,
                source_id: this.id,
            }
            Core.Api.Invoice.save(form).then(res => {
                this.$message.success('保存成功')
                this.routerChange('invoice', res.detail)
            }).catch(err => {
                console.log('handleSubmit err:', err)
            })
        },
        // 删除
        handleDelete(id) {
            let _this = this;
            this.$confirm({
                title: '确定要删除该产品吗？',
                okText: '确定',
                okType: 'danger',
                cancelText: '取消',
                onOk() {
                    Core.Api.Entity.delete({id}).then(() => {
                        _this.$message.success('删除成功');
                        _this.getTableData();
                    }).catch(err => {
                        console.log("handleDelete err", err);
                    })
                },
            });
        },
    }
};
</script>

